// 获取标签名称的辅助函数
function getGameTagName(tagId) {
    return GameTags[tagId] || `未知标签(${tagId})`;
}

// 格式化标签显示（ID + 名称）
function formatGameTag(tagId) {
    const tagName = GameTags[tagId];
    if (tagName) {
        return `${tagId} (${tagName})`;
    }
    return `${tagId}`;
}

// 导出到全局
if (typeof window !== 'undefined') {
    window.GameTags = GameTags;
    window.getGameTagName = getGameTagName;
    window.formatGameTag = formatGameTag;
}
